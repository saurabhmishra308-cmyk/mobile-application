ATTACHMENT_KINDS: dict[str, tuple[str, str, str]] = {
    # kind -> (instrument_type, format, mime)
    "flowmeter_csv": ("flowmeter", "csv", "text/csv"),
    "flowmeter_pdf": ("flowmeter", "pdf", "application/pdf"),
    "dwlr_csv": ("dwlr", "csv", "text/csv"),
    "dwlr_pdf": ("dwlr", "pdf", "application/pdf"),
}


async def _fetch_attachment(
    token: str,
    instrument_type: str,
    fmt: str,
    hardware_id: str | None,
    days: int | None,
) -> tuple[bytes, str, str]:
    """Download a report from the Envirolytics API. Returns (bytes, mime, filename)."""
    params: dict[str, str] = {"instrument_type": instrument_type, "format": fmt}
    if hardware_id:
        params["hardware_id"] = hardware_id
    if days:
        params["days"] = str(days)
    resp = await _envirolytics_client.get(
        "/api/flowmeter-mgmt/export",
        params=params,
        headers={"Authorization": f"Bearer {token}"},
    )
    if resp.status_code == 401:
        raise HTTPException(401, "Envirolytics token invalid — please sign in again.")
    if resp.status_code >= 400:
        raise HTTPException(502, f"Upstream export failed ({resp.status_code}).")
    mime = "text/csv" if fmt == "csv" else "application/pdf"
    from datetime import datetime, timezone as _tz

    stamp = datetime.now(_tz.utc).strftime("%Y-%m-%d")
    filename = f"envirolytics_{instrument_type}_{stamp}.{fmt}"
    return resp.content, mime, filename


def _build_html(intro: str, cadence: str | None = None) -> str:
    """Simple branded HTML wrapper for the email body."""
    tag = f"{cadence.title()} report" if cadence else "Report"
    return f"""
    <div style="font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif; background:#f6f7f9; padding:24px;">
      <div style="max-width:560px; margin:0 auto; background:#ffffff; border-radius:12px; overflow:hidden; border:1px solid #e5e7eb;">
        <div style="background:#1a2332; padding:20px 24px; color:#f8fafc;">
          <div style="font-size:11px; letter-spacing:3px; color:#94a3b8; font-weight:700;">ENVIROLYTICS · MONITOR</div>
          <div style="font-size:20px; font-weight:800; margin-top:4px;">{tag}</div>
        </div>
        <div style="padding:24px; color:#111827; line-height:1.5; font-size:14px;">
          {intro}
          <p style="margin-top:24px; color:#6b7280; font-size:12px;">
            Attached files are generated directly by the Envirolytics platform (monitor.envirolytics.in). Open the CSV in Excel/Google Sheets or the PDF in any viewer.
          </p>
        </div>
        <div style="padding:16px 24px; background:#f9fafb; color:#9ca3af; font-size:11px; text-align:center; border-top:1px solid #e5e7eb;">
          Sent by Envirolytics Sustainability Pvt Ltd · info@envirolytics.in
        </div>
      </div>
    </div>
    """


async def send_report_email(
    to_email: str,
    envirolytics_token: str,
    kinds: list[str],
    subject: str,
    intro_html: str,
    cadence: str | None = None,
    hardware_id: str | None = None,
    days: int | None = None,
) -> None:
    """Fetch each requested kind of report and email as SendGrid attachments."""
    if SENDGRID_API_KEY in ("", "placeholder"):
        raise HTTPException(500, "SENDGRID_API_KEY not configured on the server.")
    unknown = [k for k in kinds if k not in ATTACHMENT_KINDS]
    if unknown:
        raise HTTPException(400, f"Unknown attachment kind(s): {unknown}")
    message = Mail(
        from_email=(SENDER_EMAIL, SENDER_NAME),
        to_emails=to_email,
        subject=subject,
        html_content=_build_html(intro_html, cadence),
    )
    for kind in kinds:
        instrument_type, fmt, _ = ATTACHMENT_KINDS[kind]
        content, mime, filename = await _fetch_attachment(
            envirolytics_token, instrument_type, fmt, hardware_id, days
        )
        att = Attachment()
        att.file_content = FileContent(base64.b64encode(content).decode())
        att.file_name = FileName(filename)
        att.file_type = FileType(mime)
        att.disposition = Disposition("attachment")
        message.add_attachment(att)

    def _send_sync() -> None:
        SendGridAPIClient(SENDGRID_API_KEY).send(message)

    try:
        # SendGrid client is sync; run in the default executor.
        await asyncio.to_thread(_send_sync)
    except Exception as exc:
        logger.exception("SendGrid failed: %s", exc)
        raise HTTPException(502, "Email provider failed to send the report.")


async def _send_scheduled_reports(cadence: str) -> None:
    """Called by APScheduler for `weekly` or `monthly` cadences."""
    if SENDGRID_API_KEY in ("", "placeholder"):
        logger.warning("Skipping scheduled reports — SENDGRID_API_KEY not set.")
        return
    days = 7 if cadence == "weekly" else 30
    query = {
        "envirolytics_token": {"$exists": True, "$ne": None},
        f"subscriptions.{cadence}": True,
    }
    async for user in db.push_users.find(query):
        email = user.get("email") or user.get("user_email")
        if not email:
            continue
        try:
            await send_report_email(
                to_email=email,
                envirolytics_token=user["envirolytics_token"],
                kinds=[
                    "flowmeter_csv",
                    "flowmeter_pdf",
                    "dwlr_csv",
                    "dwlr_pdf",
                ],
                subject=f"Envirolytics {cadence} report",
                intro_html=(
                    f"<p>Hi {user.get('full_name', 'there')},</p>"
                    f"<p>Attached is your {cadence} compliance report bundle covering the last <b>{days} days</b> of instrument activity.</p>"
                ),
                cadence=cadence,
                days=days,
            )
        except Exception as exc:  # never crash the scheduler on a single failure
            logger.warning("Scheduled email failed for %s: %s", email, exc)


