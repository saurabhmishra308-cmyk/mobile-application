@api_router.post("/register-push", status_code=201)
async def register_push(body: RegisterPushBody):
    """Register the device token with the Emergent push service AND remember
    the user's Envirolytics token so the poller can check alerts on their
    behalf. Called on every app open — safe to call repeatedly.
    """
    upstream_payload = {
        "user_id": body.user_id,
        "platform": body.platform,
        "device_token": body.device_token,
    }
    resp = await _push_client.post(
        "/api/v1/push/users/register", json=upstream_payload
    )
    if resp.status_code == 401:
        raise HTTPException(500, "EMERGENT_PUSH_KEY missing or invalid")
    if resp.status_code >= 500:
        raise HTTPException(502, "Push provider unavailable")
    resp.raise_for_status()

    # Persist the user record so the poller can query alerts.
    update: dict[str, Any] = {
        "user_id": body.user_id,
        "platform": body.platform,
        "device_token": body.device_token,
        "token_invalid": False,
    }
    if body.envirolytics_token:
        update["envirolytics_token"] = body.envirolytics_token
    if body.email:
        update["email"] = body.email
    if body.full_name:
        update["full_name"] = body.full_name
    if body.role:
        update["role"] = body.role
    await db.push_users.update_one(
        {"user_id": body.user_id},
        {
            "$set": update,
            "$setOnInsert": {
                "known_offline": [],
                "known_breaches": [],
                "subscriptions": {"weekly": False, "monthly": False},
            },
        },
        upsert=True,
    )
    return {"status": "registered"}


class EmailReportBody(BaseModel):
    recipient: EmailStr
    envirolytics_token: str
    kinds: list[str] = Field(default_factory=lambda: ["flowmeter_csv", "flowmeter_pdf"])
    hardware_id: str | None = None
    days: int | None = None
    subject: str | None = None
    note: str | None = None


@api_router.post("/email-report")
async def email_report(body: EmailReportBody):
    subject = body.subject or "Envirolytics data export"
    intro = (
        f"<p>Hi,</p>"
        f"<p>{body.note or 'Here is the Envirolytics data export you requested from the mobile app.'}</p>"
    )
    await send_report_email(
        to_email=body.recipient,
        envirolytics_token=body.envirolytics_token,
        kinds=body.kinds,
        subject=subject,
        intro_html=intro,
        hardware_id=body.hardware_id,
        days=body.days,
    )
    return {"status": "sent", "recipient": body.recipient, "count": len(body.kinds)}


class SubscribeBody(BaseModel):
    user_id: str
    email: EmailStr
    envirolytics_token: str
    full_name: str | None = None
    weekly: bool = False
    monthly: bool = False


@api_router.post("/email-subscriptions")
async def set_email_subscription(body: SubscribeBody):
    await db.push_users.update_one(
        {"user_id": body.user_id},
        {
            "$set": {
                "user_id": body.user_id,
                "email": body.email,
                "envirolytics_token": body.envirolytics_token,
                "full_name": body.full_name,
                "subscriptions": {"weekly": body.weekly, "monthly": body.monthly},
            },
            "$setOnInsert": {"known_offline": [], "known_breaches": []},
        },
        upsert=True,
    )
    return {
        "status": "ok",
        "weekly": body.weekly,
        "monthly": body.monthly,
    }


@api_router.get("/email-subscriptions/{user_id}")
async def get_email_subscription(user_id: str):
    doc = await db.push_users.find_one(
        {"user_id": user_id}, {"_id": 0, "subscriptions": 1, "email": 1}
    )
    subs = (doc or {}).get("subscriptions") or {}
    return {
        "weekly": bool(subs.get("weekly")),
        "monthly": bool(subs.get("monthly")),
        "email": (doc or {}).get("email"),
    }


@api_router.post("/unregister-push")
async def unregister_push(body: dict):
    user_id = body.get("user_id")
    if not user_id:
        raise HTTPException(400, "user_id required")
    await db.push_users.delete_one({"user_id": user_id})
    return {"status": "unregistered"}


@api_router.post("/admin/run-auto-deactivate")
async def run_auto_deactivate_now():
    """Manual trigger for the daily auto-deactivate job. Returns immediately;
    logs cover the result."""
    asyncio.create_task(_auto_deactivate_expired_users())
    return {"status": "scheduled"}


