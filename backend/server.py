async def _auto_deactivate_expired_users() -> None:
    """Runs daily. For each registered admin user, ask the upstream for the
    subuser list and PUT is_active=false on anyone whose account is more than
    365 days old and still marked active.
    """
    from datetime import datetime, timezone as _tz

    admins = db.push_users.find(
        {
            "envirolytics_token": {"$exists": True, "$ne": None},
            "$or": [{"role": "admin"}, {"is_admin": True}],
        }
    )
    handled = 0
    async for adm in admins:
        token = adm.get("envirolytics_token")
        if not token:
            continue
        try:
            resp = await _envirolytics_client.get(
                "/api/admin/users/list",
                headers={"Authorization": f"Bearer {token}"},
            )
        except Exception as exc:
            logger.warning("auto-deactivate: list failed for %s: %s", adm.get("user_id"), exc)
            continue
        if resp.status_code == 401:
            await db.push_users.update_one(
                {"user_id": adm.get("user_id")},
                {"$unset": {"envirolytics_token": ""}, "$set": {"token_invalid": True}},
            )
            continue
        if resp.status_code >= 400:
            continue
        now = datetime.now(_tz.utc)
        for u in resp.json().get("users") or []:
            if not u.get("is_active"):
                continue
            created = u.get("created_at")
            if not created:
                continue
            try:
                created_dt = datetime.fromisoformat(created.replace("Z", "+00:00"))
            except Exception:
                continue
            days = (now - created_dt).days
            if days < 365:
                continue
            # Skip admins so we never lock the last operator out.
            if (u.get("role") or "").lower() == "admin":
                continue
            try:
                await _envirolytics_client.put(
                    f"/api/admin/users/{u['id']}/status",
                    params={"is_active": "false"},
                    headers={"Authorization": f"Bearer {token}"},
                )
                handled += 1
                logger.info(
                    "auto-deactivated %s (age=%dd)", u.get("email"), days,
                )
            except Exception as exc:
                logger.warning("auto-deactivate: PUT failed for %s: %s", u.get("id"), exc)
        # One admin's list is enough — every admin sees the same tenant.
        break
    logger.info("auto-deactivate cycle handled %d user(s)", handled)


@asynccontextmanager
async def lifespan(_: FastAPI):
    task = asyncio.create_task(_poller_loop())
    scheduler = AsyncIOScheduler(timezone=REPORT_TIMEZONE)
    # Weekly on Monday 06:00 IST, monthly on the 1st at 06:00 IST.
    scheduler.add_job(
        _send_scheduled_reports,
        CronTrigger(day_of_week="mon", hour=6, minute=0),
        args=["weekly"],
        id="weekly-reports",
        replace_existing=True,
    )
    scheduler.add_job(
        _send_scheduled_reports,
        CronTrigger(day=1, hour=6, minute=0),
        args=["monthly"],
        id="monthly-reports",
        replace_existing=True,
    )
    # Daily 03:00 IST auto-deactivate check for users past 365 days.
    scheduler.add_job(
        _auto_deactivate_expired_users,
        CronTrigger(hour=3, minute=0),
        id="auto-deactivate-expired",
        replace_existing=True,
    )
    scheduler.start()
    try:
        yield
    finally:
        scheduler.shutdown(wait=False)
        task.cancel()
        try:
            await task
        except asyncio.CancelledError:
            pass
        await _push_client.aclose()
        await _envirolytics_client.aclose()
        _mongo.close()


app = FastAPI(lifespan=lifespan)
api_router = APIRouter(prefix="/api")


